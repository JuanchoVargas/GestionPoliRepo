-- Create view "Usuarios"
--
CREATE VIEW "Usuarios" (
  "RoleId",
  "RoleName",
  "CompanyId",
  "CompanyName",
  "CompanyAcronym",
  "DependenceId",
  "DependenceName",
  "Id",
  "Name",
  "FirstName",
  "LastName",
  "Email",
  "UserName",
  "EmailConfirmed",
  "PasswordHash",
  "SecurityStamp",
  "PhoneNumber",
  "PhoneNumberConfirmed",
  "TwoFactorEnabled",
  "LockoutEnd",
  "LockoutEnabled",
  "AccessFailedCount",
  "ConcurrencyStamp",
  "NormalizedEmail",
  "NormalizedUserName",
  "IsActive"
) AS
    SELECT UR."RoleId",
           R."Name" AS "RoleName",
           U."CompanyId",
           C."Nombre" AS "CompanyName",
           C."Descripcion" AS "CompanyAcronym",
           U."DependenceId",
           D."Nombre" AS "DependenceName",
           U."Id",
           U."FirstName" || ' ' || U."LastName" AS "Name",
           U."FirstName",
           U."LastName",
           U."Email",
           U."UserName",
           U."EmailConfirmed",
           U."PasswordHash",
           U."SecurityStamp",
           U."PhoneNumber",
           U."PhoneNumberConfirmed",
           U."TwoFactorEnabled",
           U."LockoutEnd",
           U."LockoutEnabled",
           U."AccessFailedCount",
           U."ConcurrencyStamp",
           U."NormalizedEmail",
           U."NormalizedUserName",
           U."IsActive"
      FROM "Clasificador" D
        RIGHT OUTER JOIN "AuthUsers" U
          ON D."Id" = U."DependenceId"
        LEFT OUTER JOIN "Clasificador" C
          ON U."CompanyId" = C."Id"
        LEFT OUTER JOIN "AuthRoles" R
          RIGHT OUTER JOIN "AuthUserRoles" UR
            ON R."Id" = UR."RoleId"
          ON U."Id" = UR."UserId";

--
-- Create view "Clasificadores"
--
CREATE VIEW "Clasificadores" (
  "PadreTipoID",
  "PadreTipoNombre",
  "PadreId",
  "PadreNombre",
  "TipoId",
  "TipoNombre",
  "Id",
  "Nombre",
  "Descripcion",
  "Orden",
  "Hijos",
  "Activo",
  "CreadoEl",
  "CreadoPor",
  "EditadoEl",
  "EditadoPor"
) AS
    SELECT P."TipoId" AS "PadreTipoID",
           PT."Nombre" AS "PadreTipoNombre",
           C."PadreId",
           P."Nombre" AS "PadreNombre",
           C."TipoId",
           T."Nombre" AS "TipoNombre",
           C."Id",
           C."Nombre",
           C."Descripcion",
           C."Orden",
           (SELECT COUNT(*)
               FROM "Clasificador"
               WHERE "PadreId" = C."Id") AS "Hijos",
           C."Activo",
           C."CreadoEl",
           C."CreadoPor",
           C."EditadoEl",
           C."EditadoPor"
      FROM "Clasificador" C
        LEFT OUTER JOIN "ClasificadorTipo" T
          ON C."TipoId" = T."Id"
        LEFT OUTER JOIN "Clasificador" P
          ON C."PadreId" = P."Id"
        LEFT OUTER JOIN "ClasificadorTipo" PT
          ON PT."Id" = P."TipoId";

